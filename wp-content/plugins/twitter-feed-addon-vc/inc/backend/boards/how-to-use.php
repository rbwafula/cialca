    <h3 class="tfa-title"><?php _e('How to use', TFA_VC_DOMAIN); ?></h3>
    <p>This plugin is an addon for WPBakery Page Builder plugin. Make sure you have it installed in your site. Once you have it installed in your site. Go to the listing section for page or post(whichever you have enabled for editing)</p>
    <p>There are three different layouts defined in the plugin. All feeds have a common parent container.</p>
    <p>Once you have included the container using the editor, you can add twitter feeds while choosing the layout it should appear in.</p>

    <h4>Feeds in List</h4>
    <p>For displaying Twitter Feeds in a list format. Once you have selected the list format a default list of feeds will appear along with the necessary settings you can alter to get a different result.</p>
    
    <h4>Feeds In Slider</h4>
   <p>For displaying Twitter Feeds in a slider format. Once you have selected the slider format a default slider of feeds will appear along with the necessary settings you can alter to get a different result.</p>
       
    <h4>Feeds in ticker</h4>
    <p>For displaying Twitter Feeds in a ticker format. Once you have selected the ticker format a default ticker of feeds will appear along with the necessary settings you can alter to get a different result.</p>
   
    <p>You may also display the feeds using the hashtag attribute.</p>

    <p>To use multiple accounts within single feed, please use comma separated username without any space( ) in the username parameter.</p>
    <p>For detailed documentation please visit <a href="https://accesspressthemes.com/documentation/twitter-feed-addon-vc/" target="_blank">here</a></p>
